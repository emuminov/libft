#include "../libft.h"
#include <assert.h>
#include <stdio.h>
int	main(void)
{
	assert(ft_atol("2800000000") == 2800000000);
	assert(ft_atol("28000000000") == 28000000000);
	assert(ft_atol("-3000000000") == -3000000000);
	assert(ft_atol("0") == 0);
}
